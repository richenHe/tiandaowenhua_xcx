/** BIN 识别结果 */
export interface BinLookupResult {
  /** 识别到的发卡行名称，如"招商银行" */
  bankName: string
  /** 用于匹配的关键词列表 */
  keywords: string[]
}

/**
 * 根据银行卡号识别发卡行
 * @param cardNumber 银行卡号（允许含空格）
 * @returns 识别结果，无法识别时返回 null
 */
export function lookupBankByCardNumber(cardNumber: string): BinLookupResult | null {
  const cleaned = cardNumber.replace(/\s/g, '')
  if (cleaned.length < 6) return null

  const prefix6 = cleaned.substring(0, 6)

  for (const entry of BIN_DATA) {
    if (entry.prefixes.includes(prefix6)) {
      return { bankName: entry.bankName, keywords: entry.keywords }
    }
  }

  // 前 6 位未匹配，尝试前 4 位宽松匹配
  return null
}

/**
 * 校验开户支行名称是否与 BIN 识别出的发卡行匹配
 * @param branchName 用户填写的开户支行，如"招商银行深圳南山支行"
 * @param binResult BIN 识别结果
 * @returns true 表示匹配
 */
export function isBranchMatchingBin(branchName: string, binResult: BinLookupResult): boolean {
  const normalizedBranch = branchName.trim().toLowerCase()
  return binResult.keywords.some(kw => normalizedBranch.includes(kw.toLowerCase()))
}

/**
 * 简单校验银行卡号格式（长度 + 纯数字）
 * 注意：部分中国银行卡不符合 Luhn 算法，此处只做基本格式校验
 * @param cardNumber 银行卡号（允许含空格）
 */
export function validateCardNumberFormat(cardNumber: string): boolean {
  const cleaned = cardNumber.replace(/\s/g, '')
  if (!/^\d{10,25}$/.test(cleaned)) return false
  return true
}

/**
 * 将银行卡号脱敏（保留前 4 位和后 4 位）
 * 如 "6222021234567890" → "6222 **** **** 7890"
 */
export function maskCardNumber(cardNumber: string): string {
  const cleaned = cardNumber.replace(/\s/g, '')
  if (cleaned.length <= 8) return cleaned
  const head = cleaned.substring(0, 4)
  const tail = cleaned.substring(cleaned.length - 4)
  const middle = '*'.repeat(Math.min(cleaned.length - 8, 8))
  return `${head} ${middle} ${tail}`
}
